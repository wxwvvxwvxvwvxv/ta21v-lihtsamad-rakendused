operation = input("Please write + or - or * or / to pick calculator: ")
if operation == "+":
    number1 = int(input("Please enter nr1: "))
    number2 = int(input("Please enter nr2: "))
    print(number1 + number2)

if operation == "-":
    number1 = int(input("Please enter nr1: "))
    number2 = int(input("Please enter nr2: "))
    print(number1 - number2)

if operation == "*":
    number1 = int(input("Please enter nr1: "))
    number2 = int(input("Please enter nr2: "))
    print(number1 * number2)

if operation == "/":
    number1 = int(input("Please enter nr1: "))
    number2 = int(input("Please enter nr2: "))
    print(number1 / number2)